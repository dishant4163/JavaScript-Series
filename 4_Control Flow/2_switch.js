// Switch in JS


// basic Syntax of switch-case

// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }




// with Numbers
const month = 4

switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("Apr");
        break;
    case 5:
        console.log("May");
        break;

    default:
        console.log("default case match");
        break;
}




//with Strings

const Month = "march"

switch (Month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}


